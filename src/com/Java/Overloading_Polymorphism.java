package com.Java;

public class Overloading_Polymorphism {
	  public void methods_Overloading() {
		  System.out.println("Datatype");
	

	}
	  public void methods_Overloading(int a) {
		  System.out.println(a);
	

	}
	  public void methods_Overloading(String Madhan) {
		  System.out.println(Madhan);
	

	}
	  public void methods_Overloading(int a , int b) {
		  System.out.println(a+b);
	

	}
	 
}
